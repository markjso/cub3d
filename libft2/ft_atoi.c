#include "libft.h"

int	ft_atoi(const char *str)
{
	int	result;
	int	neg;
	int	i;

	result = 0;
	neg = 0;
	i = 0;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = 1;
	i++;
	}
	while (ft_isdigit(str[i]))
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	if (neg)
		return (-result);
	return (result);
}
